package CompiladorC;

import java_cup.runtime.Symbol;
import java_cup.symbol;

import javax.swing.*;
import javax.swing.table.DefaultTableModel;
import java.util.ArrayList;

public class ControlParser implements IControl <LexerCupAnalyzer>{
    private ArrayList<Symbol> listaParser;
    private Parser parser;
    public ControlParser() {
        listaParser = new ArrayList<>();
    }
    @Override
    public void limpiar() {
        listaParser.clear();
    }

    @Override
    public void procesar(LexerCupAnalyzer lexer) {
        try {

            parser = new Parser(lexer);
            Symbol s = parser.parse();
            listaParser.add(s);
        } catch (Exception e) {

            System.out.println("Error en el parser" + parser.error_sym());

        }
    }

    @Override
    public DefaultTableModel construirModelo(JTable tabla) {
        throw new UnsupportedOperationException();
    }
}
